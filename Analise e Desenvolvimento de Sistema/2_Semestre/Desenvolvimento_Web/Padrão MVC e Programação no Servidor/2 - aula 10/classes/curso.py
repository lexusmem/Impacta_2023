class Curso:
    def __init__(self, nome, descricao, habilidade, dificuldade):
        self.nome = nome
        self.descricao = descricao
        self.habilidade = habilidade
        self.dificuldade = dificuldade


def nome(self):
    return self.nome


def descricao(self):
    return self.descricao


def habilidade(self):
    return list(self.habilidade)


def dificuldade(self):
    return self.dificuldade
